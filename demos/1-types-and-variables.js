// In JavaScript, there are different *types* of things:

// Numbers
console.log(2);
console.log(5);

// Strings
console.log("Hello, world!");

// We can do simple math
console.log(2 + 5);

// We can even add strings together
console.log("JavaScript is " + "fun");

// Typing things again and again sucks, so we have the ability to store things
// away, using variables
let x = 10;
let y = 3;
console.log(x * y);

// We can give variables any names we want, as long as we don't use spaces
// or any weird characters. For the most part, we just use letters:
let name = "Brian";
let weather = "Hot and humid";

// Variable naming is very important. It's an art, almost. We like to name
// things so that it makes sense to whomever is working on your code in the
// future, whether that's you or someone else on your team. In JavaScript, we
// typically use a convention called camel case. That's just a funny way of saying 
// that multiple words are separated by using capital letters, which make the 
// separate words look like humps on a camel.
let firstName = "Brian";
let currentWeatherConditions = "Hot and humid";
